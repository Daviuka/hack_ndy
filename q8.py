'''8. Escreva um programa para calcular a área de um triângulo, sendo dados a sua base e a
sua altura.'''

base = int(input('Digite a base: ')) #entrada de dados e tratamento
altura = int(input('Digite a altura: '))

#operadores matemáticos
area= (base*altura)/2

print(f'O cálculo da área será igual a: {area}.')